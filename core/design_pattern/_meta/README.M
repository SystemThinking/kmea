# Design Patterns (SSOT)

Toto je centrálny „kuchársky zošit“ pre štruktúry, navigácie a pravidlá naprieč rámcami (KNIFE, Q12, 7Ds, THESEI…).

## Čo tu nájdeš
- **patterns/** – záväzné pravidlá (názvoslovie, FM-Core zásady, navigácie, šablóny).
- **examples/** – minimálne, copy-paste príklady (index.md pre sekcie, nav riadky).
- **_meta/** – slovníček pojmov, poznámky k evolúcii.

## Ako používať
1. Pred každou novou entitou si otvor „patterns“.
2. Aplikuj zodpovedajúci pattern (KNIFE/Q12/7Ds).
3. Ak je konflikt, vyhráva `01-fm-core-rules.md` (SSOT).
